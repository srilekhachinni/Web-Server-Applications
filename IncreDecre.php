<?php
//Initialize a variable
$num = 10;
echo "Initial value: $num <br>";

//Pre-increment
echo "After pre-increment (++\$num): " . ++$num . "<br>";

//Post-increment
echo "After post-increment (\$num++): " . $num++ . " (but now \$num = $num) <br>";

//Pre-decrement
echo "After pre-decrement (--\$num): " . --$num . "<br>";

//Post-decrement
echo "After post-decrement (\$num--): " . $num-- . " (but now \$num = $num) <br>";
?>
